  /// Returns the parent category name for a given topic tag. Returns null if not found.
  static String? getCategoryFromTopic(String topic) {
    final key = topic.trim().toLowerCase();
    return _mapping[key];
  }

  /// Fallback category mapper based on level numbers (1-100)
  static String getCategoryFromLevel(int level) {
    if (level >= 1 && level <= 10) {
      return 'Law';
    } else if (level >= 11 && level <= 20) {
      return 'Law';
    } else if (level >= 21 && level <= 30) {
      return 'History';
    } else if (level >= 31 && level <= 40) {
      return 'Wisdom';
    } else if (level >= 41 && level <= 50) {
      return 'Prophets';
    } else if (level >= 51 && level <= 60) {
      return 'Prophets';
    } else if (level >= 61 && level <= 70) {
      return 'Gospels';
    } else if (level >= 71 && level <= 80) {
      return 'Gospels';
    } else if (level >= 81 && level <= 90) {
      return 'New Testament';
    } else if (level >= 91 && level <= 100) {
      return 'Epistles';
    }
    return 'Old Testament';
  }

  /// Checks if a quiz category matches the selected category filter (with support for nested OT/NT categories).
  static bool matchesCategory(String quizCategory, String selectedCategory) {
    if (selectedCategory == 'All') return true;
    if (selectedCategory == 'Old Testament') {
      return quizCategory == 'Law' || quizCategory == 'History' || quizCategory == 'Wisdom' || quizCategory == 'Prophets' || quizCategory == 'Old Testament';
    }
    if (selectedCategory == 'New Testament') {
      return quizCategory == 'Gospels' || quizCategory == 'Epistles' || quizCategory == 'New Testament';
    }
    return quizCategory == selectedCategory;
  }
