def fix_punctuation(text):
    if not text:
        return text
    
    text = text.strip()
    # Replace semicolons with danda for run-on sentences
    text = text.replace(';', '।')
    # If ends with a comma, replace it with danda
    if text.endswith(','):
        text = text[:-1] + '।'
    # If no ending punctuation, add danda
    elif not text.endswith(('.', '?', '!', ';', '।', ')')):
        text = text + '।'
        
    text = re.sub(r' +', ' ', text)
    return text

def has_two_telugu_chars(text):
    count = sum(1 for c in text if '\u0c00' <= c <= '\u0c7f')
    return count >= 2

