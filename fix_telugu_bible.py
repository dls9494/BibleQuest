def main():
    print("Loading Telugu Bible JSON...")
    with open("assets/bible/telugu_ov.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    total_verses = 0
    fixed_verses = 0
    failed_validation = 0

    print("Processing verses...")
    for book, chapters in data.items():
        for ch, verses in chapters.items():
            for v, text in verses.items():
                total_verses += 1
                
                # Check mapping for specific NT prophecies/fixed text
                key = (book, ch, v)
                if key in prophecies_mapping:
                    new_text = prophecies_mapping[key]
                else:
                    # Apply general formatting
                    new_text = fix_spaces(text)
                    new_text = fix_punctuation(new_text)

                # Validate
                if not has_two_telugu_chars(new_text):
                    print(f"Validation FAILED for {book} {ch}:{v} -> '{new_text}'")
                    failed_validation += 1
                
                if new_text != text:
                    data[book][ch][v] = new_text
                    fixed_verses += 1

    print(f"Total verses checked: {total_verses}")
    print(f"Total verses fixed/modified: {fixed_verses}")
    print(f"Validation failed count: {failed_validation}")

    print("Saving back to assets/bible/telugu_ov.json...")
    with open("assets/bible/telugu_ov.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print("Done!")

if __name__ == "__main__":
    main()
